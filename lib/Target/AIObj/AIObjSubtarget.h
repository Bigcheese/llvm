#ifndef AIOBJ_SUBTARGET_H
#define AIOBJ_SUBTARGET_H

#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "AIObjGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class AIObjSubtarget : public AIObjGenSubtargetInfo {
  virtual void anchor();

public:
  AIObjSubtarget(const std::string &TT, const std::string &CPU,
                 const std::string &FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  std::string getDataLayout() const {
    return "e-S64-p:64:64:64-i64:64:64-f64:64:64-n64";
  }
};

} // end namespace llvm

#endif
